def blink_morse(message):
    for c in message:
        c = c.lower()
        if c in morse_code:
            code = morse_code[c]
            for s in code:
                if s == '.':
                    blink(200)
                elif s == '-':
                    blink(600)
                elif s == ' ':
                    time.sleep_ms(1000)
        # Space between letters
        time.sleep_ms(400)

def blink(duration):
    print(duration)
    time.sleep_ms(duration)

